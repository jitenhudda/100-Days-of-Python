# For Loop

for i in range(1,11):
    print(i)


# Table print using for loop of any number

number = int(input("enter the number "))

for i in range(1,11):
    print(number, "*", i, "=", number*i)


# for loop in Sequence/ String

for i in "HARYANA":
    print(i)

# for loop work with set, dict, tuple, list also 

for i in [1,2,3,4,5]:
    print(i)


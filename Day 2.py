## DATA TYPES

# 1 - Basic Data Types
#integer-
print(10)
print("range of integer - ", 1e308)
print(1e309)  # this shows infinity limit hit

#float-
print(4.5)

#boolean-
print(True)
print(False)

#complex-
print(4+5j)

#string-
print('hello')
print("hello")
